SELECT ddl FROM (
  SELECT 1 AS sort_order, sequence_name AS obj_name,
         DBMS_METADATA.GET_DDL('SEQUENCE', sequence_name) || CHR(10) AS ddl
  FROM user_sequences
  WHERE sequence_name NOT LIKE 'ISEQ$$_%'
  UNION ALL
  SELECT 2, table_name,
         DBMS_METADATA.GET_DDL('TABLE', table_name) || CHR(10)
  FROM user_tables
  UNION ALL
  SELECT 3, index_name,
         DBMS_METADATA.GET_DDL('INDEX', index_name) || CHR(10)
  FROM user_indexes
  WHERE index_name NOT LIKE 'SYS_%'
    AND index_type != 'LOB'
    AND index_name NOT IN (SELECT constraint_name FROM user_constraints WHERE constraint_type IN ('P','U'))
  UNION ALL
  SELECT 4, constraint_name,
         DBMS_METADATA.GET_DDL('REF_CONSTRAINT', constraint_name) || CHR(10)
  FROM user_constraints
  WHERE constraint_type = 'R'
  UNION ALL
  SELECT 5, view_name,
         DBMS_METADATA.GET_DDL('VIEW', view_name) || CHR(10)
  FROM user_views
)
ORDER BY sort_order, obj_name;

